import (
	"sort"
)

func notReservedWord(v interface{}, param string) error {
	idx := sort.SearchStrings(reservedUsernames, v.(string))
	if idx < len(reservedUsernames) &&
		reservedUsernames[idx] == v.(string) {
		//is a stop word
		return ErrUserIdForbidden
	}
	return nil

}

